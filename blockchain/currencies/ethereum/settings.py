class Settings(object, metaclass=SingletonType):
    _config = {}
    web3 = None
    contract = None
    events_filter = None

    def __getattr__(self, key):
        if key in self.__class__._config:
            return self.__class__._config[key]
        else:
            self.__getattribute__(key)

    @classmethod
    def config(cls, key):
        return cls._config[key]

    @classmethod
    def init(cls, config):
        cls._config = config
        cls.init_web3()
        cls.init_contract()

    @classmethod
    def init_web3(cls):
        cls.web3 = Web3(HTTPProvider(cls.config('rpc_url')))

    @classmethod
    def init_contract(cls):
        web3 = cls.web3
        cls.contract = web3.eth.contract(abi=CONTRACT_ABI,
                                         address=cls.config('token_address'))

    def get_events_filter(self):
        if self.events_filter is None:
            self.events_filter = getattr(self.contract.events, EVENT_NAME)\
                .createFilter(fromBlock='latest')

        return self.events_filter
